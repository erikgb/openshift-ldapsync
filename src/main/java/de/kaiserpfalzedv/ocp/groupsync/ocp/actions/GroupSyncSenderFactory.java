package de.kaiserpfalzedv.ocp.groupsync.ocp.actions;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.boot.web.client.RestTemplateBuilder;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.client.RestTemplate;

/**
 * @author klenkes {@literal <rlichti@kaiserpfalz-edv.de>}
 * @version 1.0.0
 * @since 2017-09-10
 */
@Configuration
public class GroupSyncSenderFactory {
    private static final Logger LOG = LoggerFactory.getLogger(GroupSyncSenderFactory.class);

    @Bean
    public RestTemplate restTemplate(RestTemplateBuilder builder) {
        RestTemplate result = builder.build();

        return result;
    }
}
